public class Main {
    public static void main(String[] args) {
        System.out.println("Hello world!");

        for(int i=0;i<21;i++){
            System.out.println(i);
        }

        int i = 0;
        while(i<10){
            i++;
            System.out.println(i);
        }

        int j=0;
        do{
            System.out.println(j);
            j++;
        }while(j<5);
    }
}